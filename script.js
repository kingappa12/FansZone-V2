function commander(maillot){

    const taille = document.getElementById(maillot).value;

    const version = document.getElementById("version-"+maillot).value;

    const produit = produits.find(p=>p.nom===maillot);

    let prix = produit.fan;

    if(version==="player"){
        prix = produit.player;
    }

   panier.push({

nom: maillot,
taille: taille,
version: version,
prix: prix,
quantite:1

});

    afficherPanier();

}
const recherche = document.getElementById("searchInput");

recherche.addEventListener("keyup", () => {

const texte = recherche.value.toLowerCase();

const cartes = document.querySelectorAll(".card");

cartes.forEach(c => {

c.style.display = c.innerText.toLowerCase().includes(texte)
? "block"
: "none";

});

});
function afficherPanier(){

    const cartItems = document.getElementById("cart-items");
    const cartTotal = document.getElementById("cart-total");

    cartItems.innerHTML = "";

    let total = 0;

    panier.forEach((item,index)=>{

        total += item.prix * (item.quantite || 1);

        cartItems.innerHTML += `

        <div class="cart-item">

            <div>

                <strong>${item.nom}</strong><br>

                ${item.version=="fan"?"👕 Fan":"⭐ Player"}<br>

                Taille : ${item.taille}

            </div>

            <div>

                <button onclick="moins(${index})">➖</button>

                <b>${item.quantite || 1}</b>

                <button onclick="plus(${index})">➕</button>

                <br><br>

                <b>${item.prix*(item.quantite||1)} FCFA</b>

            </div>

        </div>

        `;

    });

    if(panier.length===0){

        cartItems.innerHTML="<p>Votre panier est vide.</p>";

    }

    cartTotal.innerHTML="💰 Total : "+total+" FCFA";

    document.getElementById("cart-count").innerText=panier.length;

}


   
function togglePanier(){

    const panier = document.querySelector(".cart-box");

    if(panier.style.display === "block"){
        panier.style.display = "none";
    }else{
        panier.style.display = "block";
    }

}
document.getElementById("order-btn").addEventListener("click", () => {

    if (panier.length === 0) {
        alert("Votre panier est vide.");
        return;
    }

    let message = "Bonjour Fans Zone 👋\n\nJe souhaite commander :\n\n";

    panier.forEach(item => {
        message += `🏆 ${item.nom}
x${item.quantite} - Taille ${item.taille} - ${item.prix*item.quantite} FCFA`;
    });

   const total = panier.reduce((s, p) => s + (p.prix * p.quantite), 0);

    message += `\n💰 Total : ${total} FCFA`;

    window.open(
        `https://wa.me/22374878819?text=${encodeURIComponent(message)}`,
        "_blank"
    );

});
function plus(index){

    panier[index].quantite++;

    afficherPanier();

}

function moins(index){

    panier[index].quantite--;

    if(panier[index].quantite<=0){

        panier.splice(index,1);

    }

    afficherPanier();

}
